namespace Library
{
    /// <summary>
    /// Styles of pages appearing/disappearing
    /// </summary>
    public enum PageAnimation
    {
        /// <summary>
        /// No animation takes place
        /// </summary>
        None = 0,

        /// <summary>
        /// The oage slides and fades in from the right
        /// </summary>
        SlideAndFadeInFromRight = 1,

        /// <summary>
        /// The page flips and fades out to the left
        /// </summary>
        FlipAndFadeOut = 2
    }

}
